           Men and women from every branch of the military are  answering the call to protect the freedoms that make this nation great.&nbsp; But the call to action is not for them to  answer alone.&nbsp; It is up to each of us to  do our part to support our military, and perhaps more importantly, to support  the families of those who have left for foreign soil.<br />
           <br />

          <p>Operation Spokane Heroes (OSH) is designed to help, as a  community, to do just that.&nbsp; By showing  our support to military families we let them know they are important to the  Spokane Region and our Country.&nbsp;&nbsp; OSH has  helped families of deployed service personnel from the National Guard, Reserves  and Active Duty with gift bags for their children at Christmas.&nbsp; Two years ago, with the help of many area  residents we sent sweat shirts, shorts, pants, flip-flops, t-shirts, towels,  and basketballs to the hospital in Germany for our injured troops.&nbsp; We financially support the much needed  picnics and programs for our heroes. &nbsp;Sometimes  we help with their rent or out of town expenses for families to be with their  veteran while hospitalized out of our area.&nbsp;  Just recently we found lodging for a veteran and her two children until  housing became available, plus clothed the three of them.&nbsp; There is always more that can be done.</p>
          <p>Operation Spokane Heroes meets monthly to learn about the  needs of our veterans and find solutions.&nbsp;  If you have time, talent and resources please join us.</p>
