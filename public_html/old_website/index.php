    <div class="box">  <div class="PostHeader2">Get Involved!
      </div>
  
      <p><br />
Men and women from every branch of the military  are answering the call to protect the freedoms that  make this nation great, but the call to action is not  for them to answer alone. It is up to each of us to do  our part to support our military, and perhaps more  importantly, to support the families of those who  have left for foreign soil. <br />
<br />
OSH is designed to help us, as a community, do just  that. By showing our support to military families,  we let them know their importance to the Spokane  Region and our country. This past year OSH helped  families of deployed service personnel from the National  Guard, Reserves and Active Duty. There is always  more that can be done.<br />
<br />
